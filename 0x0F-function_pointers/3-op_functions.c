#include "3-calc.h"
/**
 * op_add - sum two numbers
 * @a: first argument
 * @b: second argument
 * Return: return the result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract two numbers
 * @a: first argument
 * @b: second argument
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two numbers
 * @a: first argument
 * @b: second argument
 * Return: the result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division on numbers
 * @a: numenator
 * @b:denomenator
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulation of two numbers
 * @a: first argumet
 * @b: second argument
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
